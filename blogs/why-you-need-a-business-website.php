				<div class="blogContent">
					<div class="blogPrologue">
						<p>So, you are asking yourself questions such as “Do I need a business website?” and “What benefits will a business website bring?”. Flurry no more, as we can help bring light to these very questions.</p>
						<p>The short answer is that there has never been a better or more important time to take your business to the next level and build yourself a website. Or, one-up on that and hire a web designer to do the work for you (More on that later). A website offers a wide array of benefits to businesses of all sizes, big or small. These benefits increase in value dramatically year on year, just like the size of the internet itself.</p>
						<p>I hear that voice in your head still questioning the need for a business website. Well, read on and let’s see if we can solidify your need for a website.</p>
					</div>

					<article class="blogSectionStandard">
						<h3>A business website makes you look professional</h3>
						<p><a href="https://blog.verisign.com/getting-online/verisign-2015-online-survey-97-percent-of-smbs-would-recommend-having-a-website-to-other-smbs/" target="_blank">84% of today’s consumers think a website makes your business more credible</a> than companies who solely use social media profiles/pages. Your website is also the perfect place to show off any awards and certificates your business has.</p>
						<p>An added extra to having a business website is that you can also have custom branded email address’ (e.g. James@yoursite.com) which adds a level professionalism to all of your correspondence, even more so if you have been using a personal email address up until now (e.g. james@gmail.com).</p>
					</article>

					<article class="blogSectionStandard">
						<h3>A website ensures your success over the long term</h3>
						<p><a href="https://www.statista.com/statistics/617136/digital-population-worldwide/" target="_blank">As of January 2021, there are 4.66 BILLION people using the internet.</a> To put that in hindsight, that is 59.5% of the global population. And that is increasing more and more everyday, especially with Elon Musks new “Starlink” coming into effect.</p>
						<img src="../img/peopleOnlineBlog.jpg" alt="Total people online 2021 statistical graph">
						<p>Without a website your business would be invisible to all of these people. As we progress further into the rabbit hole that is the digital era, your business will become invisible to everyone unless you invest in a website.</p>
						<p>So to stay relevant and competitive in this increasingly digital world you need to get your business online. Sooner rather than later.  </p>
					</article>



					<article class="blogSectionStandard">
						<h3>Your website will attract new customers through Google</h3>
						<p>This one is pretty self-explanatory but: your business website will bring in new and existing customers from Google and other search engines (Bing, grrrr). You may be happy with your current customer volume but to encourage continued success, you need to attract new customers, and the very best and easiest way is to make yourself visible on Google.</p>
						<p>A well-optimized website can help your business rank well for a variety of search terms and create a steady stream of customers. You can even go all out and start running Google ads, something you cant really capitalize on without a business website.</p>
					</article>

					<article class="blogSectionStandard">
						<h3>A website can encourage customers to contact you</h3>
						<p>Having all of your contact details readily available to everyone online increases the chances of them contacting you. If you look in the footer of practically any website you will see all contact details are available. If you are reading this blog on a computer or tablet you will see my email fixed to the left of the screen. (Give it a click)</p>
					</article>

					<article class="blogSectionStandard">
						<h3>You can clearly showcase your services and products</h3>
						<p>You can show potential customers what to expect when they work with you. Whether you are a restaurant, builder or an E-commerce store (A store that sells things online, like Amazon) You can clearly display your work, see it as an online portfolio if you wish, selling yourself to your customers without having to go out and look for them.</p>
						<p>Take a look at how the UK restaurant <a href="https://www.warehouselondon.com/" target="_blank">Warehouse</a> has accomplished this on their website:</p>
						<img src="../img/warehouseBlog.jpg" alt="">
						<p>Another thing you can do is publish important information about your products and services to your website to make sure you are attracting the correct audience. For example, if you are a restaurant you make want to include things like allergies on an online menu. Making it easier for people with allergens see that they can eat at your restaurant. The same applies to vegan-friendly options. </p>
					</article>



					<article class="blogSectionStandard">
						<h3>You can easily display customer reviews</h3>
						<p>Displaying your best customer reviews and/or testimonials on your business website is a great way to add social proof. This may come in the form of personal customer testimonials, like this review on the Steamlabs website:</p>
						<img src="../img/Steamlabs-Review.png" alt="">
						<p>If your business has been featured articles from newspapers, or blogs then you can also feature these on your website too. Again, adding to credibility and social proof.</p>
					</article>



					<article class="blogSectionStandard">
						<h3>Establishing yourself and your business</h3>
						<p>If your competitors aren’t online yet, they will be soon. Establish yourself as the leading brand in your industry by getting to the online world with a high-quality website first.</p>
						<p>If your competitors are online, you have a different kind of opportunity; one where you can build your business website completely different and stand apart from them. Showing your customers what makes you stand out.</p>
					</article>


					<article class="blogSectionStandard">
						<h3>You can integrate google maps for customers to find you more easily</h3>
						<p>A website lets you embed Google maps directly into your content. Some businesses, like Steamlabs, have a map embedded on their home page:</p>
						<img src="../img/Steamlabs-Map.png" alt="Streamlabs google maps on home screen image">
						<p>A map makes it easier for potential customers to find you, thus in turn increasing trust towards your brand (You wouldn’t want do business with someone if there was nowhere to turn of something went wrong) This is great if you host events, as they might attract people from all over the place; people who wouldn’t otherwise be in your neighborhood.</p>
						<p>As an added bonus you can even add a “Directions” button that would open up google maps as a Sat-Nav.</p>
					</article>

					<article class="blogSectionStandard">
						<h3>Can I build a website myself?</h3>
						<p>Of course! It is the 21st century after all. There is a plethora of options to choose from. A few examples of business website creators are: WordPress, Wix and Squarespace. Those are just three of the most popular ones. If you want increased functionality I would go with WordPress.</p>
						<img src="../img/wordpressBlog.png" alt="">
						<p>WordPress is the most popular and it is 100% free, of course you will still have to pay for website hosting at a monthly or yearly fee; but that is a small price to pay for the many benefits that come with a business website.</p>
					</article>

					<article class="blogSectionStandard">
						<h3>What are the benefits of hiring a web designer?</h3>
						<p>Sometimes time isn’t on your side, you may not be able to build your own website; or you just want your website to stand out from the crowd and make eyes turn.</p>
						<p>This would be when you get a web designer involved, someone that can make a standard brochure style website come to life and pop with vibrance. Because, who wants a boring website? Not you, and definitely not your potential customers!</p>
						<p>You may just want some extra functionality that website builders don’t have, or you don’t know how to implement it. There is nothing wrong with hiring a professional to build your website for you. For a FREE no obligation quote: Click Here.</p>
						<img src="../img/webDevBlog.jpg" alt="">
					</article>

					<article class="blogSectionStandard">
						<h3>How will I know what a good design is for my website?</h3>
						<p>If you have a look across the internet you will notice that no two websites are exactly the same. Some websites do take parts of websites and tweak them, this is alright. I suppose you could say “When you know you know”. Its a lot like clothes shopping, you wouldn’t buy a new pair of pants and absolutely hate them, you do have input. In fact your input is the only input that matters! The same applies to website design.</p>
						<p>If you do build the website yourself you will have the same amount of control over it as you would with hiring a web developer. You are just hiring someone else to do it professionally.</p>
					</article>

					<article class="blogSectionSummary">
						<h3>The final verdict</h3>
						<p>I am hoping by now you realise it isn’t a question of “Do I need a business website” but rather “Can I afford not to have a website for my business”. And to answer that question, no, you cannot afford not to have a website if you want your business to continue thriving in an ever expanding digital world. A business website is one of the best investments any business can make in its future.</p>
						<p>If you are ready to get started you can search the web for website builders but my recommendation is WordPress, it has tonnes of functionality and is FREE! On the other hand you could contact me: <a href="../contact.php">here.</a></p>
					</article>
