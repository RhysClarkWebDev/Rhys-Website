					<article class="blogSectionStandard">
						<h3>1. Organic Search is the primary source of website traffic</h3>
						<p>Organic search is the leading part of most online businesses website performance and a critical component of the sales funnel, and ultimately getting users to complete a conversion or engagement.</p>
						<p>As marketers know, and now you, Google owns the majority of the search market. <a href="https://www.statista.com/statistics/216573/worldwide-market-share-of-search-engines/" target="_blank">(92.26% to be precise)</a> Much higher than competitors such as Yahoo, Bing and DuckDuckGo.</p>
						<img src="../img/search-engine-market-share-2021.png" alt="search-engine-market-share-2021">
						<p>That is not to say that all search engines don’t contribute to a websites visibility because they do. It is just that Google owns a considerable higher amount of the overall search market.</p>
						<p>The reason I mention this is because you are going to have to follow Googles guidelines for the best SEO results. We know that a clear majority of the world with access to the internet is visiting Google at least once a day to get information. Being highly visible as a trusted resource by Google and other search engines will always work in a brand’s favour. Quality SEO and a high-quality website take brands there.</p>
					</article>



					<article class="blogSectionStandard">
						<h3>2. Visibility and Rankings</h3>
						<p>One of the most important functions of SEO is increasing your online properties visibility online, which means making it easier for potential customers to find you when they search for something you have to offer / something you have targeted your SEO towards. Visibility is directly related to your ranking.</p>
						<p>Imagine we take the search term “Web Developer” and we have targeted our SEO so well that we get the very best visibility on Google, this means that we will take the number 1 spot at the top of the Google page, with the exemption of paid ads.</p>
						<p>Take a look at what percentage of users click where on Google:</p>
						<img src="../img/first-page-of-google-by-the-numbers.png" alt="first-page-of-google-by-the-numbers">
						<p>The higher you rank on the search engine result page (SERP), the more likely potential customers will see you and click on through to your website, so it is of great importance to increase your organic page ranking. The more effective your SEO efforts, the higher your results / ranking will be on the SERP. This is especially important when you consider that a quarter of web users never click past the first SERP. And can you remember the last time you clicked past the first page of the Google search results?</p>
					</article>



					<article class="blogSectionStandard">
						<h3>3. Page authority</h3>
						<p>Page authority (PA) is increasingly more important to search engines as it is becoming more relevant to web users. Authority means that your website is: Trustworthy, High Quality, Relevant and has something to offer to the user.</p>
						<img src="../img/Page-Authority_Moz.png" alt="Page Authority Moz Checker Example">
						<p>Scores for Page Authority range from 1 to 100 and the higher your number, the more domain authority your site has. You can find your PA rating by using tools such as <a href="https://moz.com/link-explorer#index" target="_blank">MOZ</a> and <a href="https://neilpatel.com/ubersuggest/" target="_blank">UberSuggest</a>. While it is difficult to influence it, many SEO experts think that it is to do with the amount of backlinks your website has. More so from credible and well visited sites. There are ways and means to influence this.</p>
					</article>



					<article class="blogSectionStandard">
						<h3>4. Creating a better visitor experience</h3>
						<p>Another reason that SEO is critical is because all the time you put into creating great content and on page SEO (Keywords) improves the usability and user experience. This creates a seamless and positive customer experience.</p>
						<p>For instance, when you make your website responsive (Your website needs to be!), it will make it usable for all types of devices no matter the shape or size. Take mobile for example, with a smaller screen you wouldn’t want to be using the desktop version of a website, that causes all sorts of errors and creates a bad user experience, this negatively affects SEO. </p>
						<img src="../img/five-essential-elements-of-user-experience-design.jpg" alt="five-essential-elements-of-user-experience-design">
						<p>Similarly, by increasing your load speed, not just for countries with fast internet; but also take into account third world countries that will also have access to your website. You will reduce bounce rate and encourage users to spend longer on your website. Consumers expect a website to load as quickly as possible, whether that be on their home internet or mobile data. Three seconds load time is the limit. To view your website you can use free tools such as GTMetrix. Or, alternatively you can get a free website speed audit done by me: <a href="../contact.php">here.</a></p>
					</article>



